public class Cerveza {
    private String nombre;
    private double graduacion;

    public Cerveza(String nombre, double graduacion) {
        this.nombre = nombre;
        this.graduacion = graduacion;
    }

    public String getNombre() {
        return nombre;
    }

    public double getGraduacion() {
        return graduacion;
    }
    
}
