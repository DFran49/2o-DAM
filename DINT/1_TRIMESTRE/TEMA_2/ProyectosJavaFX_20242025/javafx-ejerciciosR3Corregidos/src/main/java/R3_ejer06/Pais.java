public class Pais {
    
    String nombre;
    Double prob;

    public Pais(String nombre, Double prob) {
        this.nombre = nombre;
        this.prob = prob;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public Double getProb() {
        return prob;
    }

    public void setProb(Double prob) {
        this.prob = prob;
    }
    
    
    
}
